    def _generate_and_fix_section_batch(
        self,
        *,
        codes: list[str],
        section_name: str,
        archetype: str,
        diagnosis_context: str,
        referral_text: str,
        scribe_section: str,
        medication_json: str,
        gap_context: str,
        has_scribe: str,
        model_id: str,
        audit_context: str | None = None,
    ) -> list[dict]:
        """Generate a gold standard batch then validate and fix if needed.

        Implements the Generate → Validate → Fix loop for Step 6 section batches:
          1. LLM call with section-specific focused prompt.
          2. Python validators check the flat output dict.
          3. If violations found, one LLM fix call patches only the affected fields.

        Returns a list of item dicts consistent with the existing _run_llm_batch contract.
        """
        raw_items = self._run_llm_batch(
            codes=codes,
            section_name=section_name,
            archetype=archetype,
            diagnosis_context=diagnosis_context,
            referral_text=referral_text,
            scribe_section=scribe_section,
            medication_json=medication_json,
            gap_context=gap_context,
            has_scribe=has_scribe,
            model_id=model_id,
            audit_context=audit_context,
        )

        # Build flat {code: value} for validators
        flat = {
            item["item_code"]: item.get("value")
            for item in raw_items
            if isinstance(item, dict) and "item_code" in item
        }
        violations = validate_batch(flat, section_name)
        if not violations:
            return raw_items

        # ── Fix pass ─────────────────────────────────────────────────────────
        logger.warning(
            "Step 6 section=%s: %d violation(s) — running fix pass",
            section_name,
            len(violations),
        )
        fix_prompt = GOLD_SECTION_FIX_PROMPT.format(
            section_name=section_name,
            violations_list="\n".join(f"- {v}" for v in violations),
            original_items_json=json.dumps(flat, indent=2),
            archetype=archetype,
            diagnosis_context=diagnosis_context,
            medication_json=medication_json,
            gap_context=gap_context,
        )
        try:
            fix_response = self.bedrock.invoke_json(
                prompt=fix_prompt,
                model_id=model_id,
                max_tokens=2048,
            )
            fix_raw = json.loads(extract_json_object(fix_response["text"].strip()))
        except Exception as exc:
            logger.error("Step 6 section=%s fix parse failed (%s) — keeping original", section_name, exc)
            return raw_items

        if not isinstance(fix_raw, dict):
            return raw_items

        # Build an index of existing items for fast lookup
        item_index = {item["item_code"]: item for item in raw_items if "item_code" in item}
        for code, corrected_value in fix_raw.items():
            code_upper = code.strip().upper()
            if code_upper in item_index:
                item_index[code_upper]["value"] = corrected_value
                item_index[code_upper]["rationale"] = (
                    item_index[code_upper].get("rationale", "")
                    + f" [Fix applied: {', '.join(violations)}]"
                )
            else:
                # New field injected by fix (e.g. missing N0415 sub-code)
                item_index[code_upper] = {
                    "item_code": code_upper,
                    "value": corrected_value,
                    "rationale": f"Added by fix pass: {', '.join(violations)}",
                    "confidence": "high",
                    "source": "fix",
                }
        logger.info("Step 6 section=%s: fix pass applied %d correction(s)", section_name, len(fix_raw))
        return list(item_index.values())

    def _run_llm_batch(
        self,
        *,
        codes: list[str],
        section_name: str,
        archetype: str,
        diagnosis_context: str,
        referral_text: str,
        scribe_section: str,
        medication_json: str,
        gap_context: str,
        has_scribe: str,
        model_id: str,
        audit_context: str | None = None,
    ) -> list[dict]:
        """Run one LLM batch and return a list of OASIS item dicts.

        Args:
            codes: List of OASIS item codes for this batch.
            section_name: Human-readable batch name used in logging and prompt.
            archetype: Patient clinical archetype string.
            diagnosis_context: Primary diagnosis description.
            referral_text: Full referral packet text.
            scribe_section: Formatted ambient scribe text block (or placeholder string).
            medication_json: Compact medication list summary string.
            gap_context: Step 4 gap context block string.
            has_scribe: "Yes" or "No" string for the prompt.
            model_id: AWS Bedrock model identifier.

        Returns:
            List of item dicts (item_code, value, rationale, confidence, source).
        """
        # Select focused section prompt; fall back to monolithic prompt for unknown sections
        prompt_template = GOLD_SECTION_PROMPT_MAP.get(section_name, OASIS_GOLD_STANDARD_PROMPT)

        # Build format kwargs — Batch E needs {medication_json}; others typically don't.
        fmt_kwargs: dict = {
            "archetype": archetype,
            "diagnosis_context": diagnosis_context,
            "has_scribe": has_scribe,
            "section_name": section_name,
            "referral_text": referral_text,
            "scribe_section": scribe_section,
            "gap_context": gap_context,
            "field_codes_json": json.dumps(codes, indent=2),
        }
        if "{medication_json}" in prompt_template:
            fmt_kwargs["medication_json"] = medication_json

        prompt = prompt_template.format(**fmt_kwargs)

        # Append medication list for batches that don't already embed it (A–D).
        # RAG is skipped in patient-dataset-generation — all source docs are injected
        # directly. Medication is a source doc and must reach every batch since it
        # affects fields across all sections (e.g. M1033 risk factors, M1400 dyspnea).
        if "{medication_json}" not in prompt_template and medication_json:
            prompt += f"\n\n--- MEDICATION LIST (reference for all fields) ---\n{medication_json}\n"

        # Append per-field OASIS coding guidance extracted from oasis_config.json.
        # This injects the same group_guidance used by oasis-llm-processor and
        # prompt-improvement into the gold-standard generation step so the LLM
        # produces consistent, rule-conformant values.
        oasis_guidance_block = self._build_oasis_guidance_for_codes(codes)
        if oasis_guidance_block:
            prompt += (
                "\n\n--- OASIS CODING GUIDANCE (per-field rules from oasis_config) ---\n"
                + oasis_guidance_block
                + "\n"
            )

        if audit_context:
            prompt += f"\n\n{audit_context}"

        response = self.bedrock.invoke_json(
            prompt=prompt,
            model_id=model_id,
            max_tokens=4096,
        )
        text = response["text"].strip()

        try:
            raw = json.loads(extract_json_object(text))
        except Exception as exc:
            logger.error(
                "Step 6 batch %s JSON parse failed (%s) — attempting repair",
                section_name,
                exc,
            )
            raw = repair_truncated_json(text)

        def _flatten_data(d: Any, prefix: str = "") -> dict[str, str]:
            flat_dict = {}
            if isinstance(d, dict):
                for k, v in d.items():
                    flat_dict.update(_flatten_data(v, prefix + k))
            elif isinstance(d, list):
                for i, item in enumerate(d):
                    flat_dict.update(_flatten_data(item, f"{prefix}_{i+1}"))
            else:
                if d is not None and str(d).strip() != "":
                    flat_dict[prefix] = str(d)
            return flat_dict

        if not isinstance(raw, list):
            logger.warning("Step 6 batch %s: LLM returned non-array — wrapping", section_name)
        if isinstance(raw, dict):
            # Flatten any nested structures into flat standard key-value map
            flat_raw = _flatten_data(raw)
            # Re-map to the list of dictionaries that the rest of the function evaluates
            raw_list = [{"item_code": k, "value": v} for k, v in flat_raw.items()]
        elif isinstance(raw, list):
            # Fallback if LLM inexplicably returned a list
            raw_list = []
            for item in raw:
                if isinstance(item, dict) and "item_code" in item and "value" in item:
                    raw_list.append(item)
                else:
                    flat_item = _flatten_data(item)
                    for k, v in flat_item.items():
                        raw_list.append({"item_code": k, "value": v})
        else:
            raw_list = []
            logger.warning("Step 6 batch %s: LLM returned unexpected structure.", section_name)

        raw = raw_list

        seen_codes: set[str] = set()
        batch_items: list[dict] = []
        for entry in raw:
            if not isinstance(entry, dict):
                continue
            code = str(entry.get("item_code") or "").strip().upper()
            if not code or code in seen_codes:
                continue
            seen_codes.add(code)
            batch_items.append({
                "item_code": code,
                "value": entry.get("value"),
            })

        # Fill in placeholder items for any codes not returned by the LLM.
        for code in codes:
            if code not in seen_codes:
                logger.warning(
                    "Step 6 batch %s: no item for %s — inserting placeholder",
                    section_name,
                    code,
                )
                batch_items.append({
                    "item_code": code,
                    "value": None,
                    "rationale": f"Not returned by LLM for batch {section_name}",
                    "confidence": "low",
                    "source": "llm",
                })

        return batch_items

    def _apply_skip_logic(
        self, item_map: dict[str, dict]
    ) -> tuple[dict[str, dict], list[str]]:
        """Apply 8 deterministic CMS skip-logic rules.

        Args:
            item_map: Dict of item_code -> item dict.

        Returns:
            Tuple of (modified item_map, list of rule names applied).
        """
        rules_applied: list[str] = []

        # ── 1. BIMS_GATE: C0100=0 → null BIMS sub-scores ─────────────────────────────────
        # Only apply when C0100 was generated by the LLM — if it was copied verbatim from
        # gap_answers (source="gap_answers"), gap is authoritative and we preserve sub-scores.
        c0100_item = item_map.get("C0100")
        c0100_from_gap = c0100_item and c0100_item.get("source") == "gap_answers"
        if _item_value(item_map, "C0100") == "0" and not c0100_from_gap:
            bims_downstream = [
                "C0200", "C0300A", "C0300B", "C0300C",
                "C0400A", "C0400B", "C0400C", "C0500",
            ]
            modified = any(
                item_map[c].get("value") is not None
                for c in bims_downstream
                if c in item_map
            )
            for code in bims_downstream:
                if code in item_map:
                    item_map[code]["value"] = None
                    item_map[code]["rationale"] = "Skip: C0100=0 (BIMS was not administered)"
            if modified:
                rules_applied.append("BIMS_GATE")

        # ── 2. PHQ_GATE: D0150A1=9 AND D0150B1=9 → null all downstream PHQ ──────────────
        if _item_value(item_map, "D0150A1") == "9" and _item_value(item_map, "D0150B1") == "9":
            phq_downstream = [
                "D0150C1", "D0150C2", "D0150D1", "D0150D2",
                "D0150E1", "D0150E2", "D0150F1", "D0150F2",
                "D0150G1", "D0150G2", "D0150H1", "D0150H2",
                "D0150I1", "D0150I2", "D0160",
            ]
            for code in phq_downstream:
                if code in item_map:
                    item_map[code]["value"] = None
                    item_map[code]["rationale"] = (
                        "Skip: PHQ-2 screening negative (D0150A1=9, D0150B1=9)"
                    )
            rules_applied.append("PHQ_GATE")

        # ── 3. PHQ_COL2_GATE: each D0150X1=0 → D0150X2=null ─────────────────────────────
        phq_col2_modified = False
        for letter in ["A", "B", "C", "D", "E", "F", "G", "H", "I"]:
            col1 = f"D0150{letter}1"
            col2 = f"D0150{letter}2"
            if _item_value(item_map, col1) == "0" and col2 in item_map:
                item_map[col2]["value"] = None
                item_map[col2]["rationale"] = (
                    f"Skip: {col1}=0 (symptom not present; frequency column N/A)"
                )
                phq_col2_modified = True
        if phq_col2_modified:
            rules_applied.append("PHQ_COL2_GATE")

        # ── 4. M1306_ULCER: M1306=0 → wound staging items = "0" ─────────────────────────
        if _item_value(item_map, "M1306") == "0":
            for code in ["M1311", "M1312", "M1313", "M1314"]:
                if code in item_map:
                    item_map[code]["value"] = "0"
                    item_map[code]["rationale"] = (
                        "Skip: M1306=0 (no unhealed pressure ulcer at Stage 2+)"
                    )
            rules_applied.append("M1306_ULCER")

        # ── 5. M1330_STASIS: M1330=0 → null M1332, M1334 ────────────────────────────────
        if _item_value(item_map, "M1330") == "0":
            for code in ["M1332", "M1334"]:
                if code in item_map:
                    item_map[code]["value"] = None
                    item_map[code]["rationale"] = "Skip: M1330=0 (no stasis ulcer present)"
            rules_applied.append("M1330_STASIS")

        # ── 6. GG_WALK_HIERARCHY: GG0170I1 unable → skip J/K/L walk items ────────────────
        # Only override items that were NOT already sourced from gap_answers (gap is authoritative).
        i1_val = _item_value(item_map, "GG0170I1")
        if i1_val in {"07", "09", "10", "88"}:
            walk_modified = False
            for code in ["GG0170J1", "GG0170K1", "GG0170L1"]:
                target = item_map.get(code)
                if target and target.get("source") != "gap_answers":
                    target["value"] = "88"
                    target["rationale"] = (
                        f"Skip: GG0170I1={i1_val} — patient cannot walk 10 ft; "
                        "longer walking activities not attempted"
                    )
                    walk_modified = True
            if walk_modified:
                rules_applied.append("GG_WALK_HIERARCHY")

        # ── 7. GG_STAIR_HIERARCHY: GG0170M1 unable → skip N stair items ──────────────────
        # NOTE: GG0170O (Picking Up Object) is NOT a stair task — it is independent of M.
        # Only override GG0170N1 and only when not sourced from gap_answers.
        m1_val = _item_value(item_map, "GG0170M1")
        if m1_val in {"07", "09", "10", "88"}:
            stair_modified = False
            for code in ["GG0170N1"]:
                target = item_map.get(code)
                if target and target.get("source") != "gap_answers":
                    target["value"] = "88"
                    target["rationale"] = (
                        f"Skip: GG0170M1={m1_val} — patient cannot climb 4 steps; "
                        "12-step stair activity not attempted"
                    )
                    stair_modified = True
            if stair_modified:
                rules_applied.append("GG_STAIR_HIERARCHY")

        # ── 8. M1740_EXCLUSIVE: code "7" (None of above) is exclusive ────────────────────
        m1740_val = _item_value(item_map, "M1740")
        if m1740_val is not None and "7" in str(m1740_val):
            if "M1740" in item_map:
                item_map["M1740"]["value"] = "7"
                item_map["M1740"]["rationale"] = (
                    "M1740 None-of-above (7) is exclusive: other behavioral symptoms cleared"
                )
            rules_applied.append("M1740_EXCLUSIVE")

        return item_map, rules_applied

    def _build_validation(self, items: list[dict], rules_applied: list[str]) -> dict:
        """Build the validation block from the final item list.

        Args:
            items: Final sorted list of OASIS item dicts.
            rules_applied: List of skip-logic rule names that were applied.

        Returns:
            Dict with bims_verified, bims_score, phq_verified, phq_score,
            hip_score, hip_valid, skip_logic_applied.
        """
        item_map = {i["item_code"]: i for i in items}

        # BIMS score from C0500
        bims_score: int | None = None
        bims_verified = False
        c0500 = item_map.get("C0500")
        if c0500 and c0500.get("value") is not None:
            try:
                bims_score = int(str(c0500["value"]).strip().split()[0])
                bims_verified = 0 <= bims_score <= 15
            except (ValueError, TypeError):
                pass

        # PHQ score from D0160
        phq_score: int | None = None
        phq_verified = False
        d0160 = item_map.get("D0160")
        if d0160 and d0160.get("value") is not None:
            try:
                phq_score = int(str(d0160["value"]).strip().split()[0])
                phq_verified = 0 <= phq_score <= 27
            except (ValueError, TypeError):
                pass

        # HIP score from GG0130A1–E1
        hip_score, hip_valid = self._calculate_hip(item_map)

        return {
            "bims_verified": bims_verified,
            "bims_score": bims_score,
            "phq_verified": phq_verified,
            "phq_score": phq_score,
            "hip_score": hip_score,
            "hip_valid": hip_valid,
            "skip_logic_applied": rules_applied,
        }

    @staticmethod
    def _calculate_hip(item_map: dict[str, dict]) -> tuple:
        """Calculate HIP score: sum of GG0130A1–E1 self-care admission performance codes (5–30).

        Returns:
            Tuple of (hip_score: int | None, hip_valid: bool).
        """
        scores: list[int] = []
        for code in HIP_CODES:
            item = item_map.get(code)
            if item is None or item.get("value") is None:
                return None, False
            try:
                val = int(str(item["value"]).strip().split()[0])
                if not (1 <= val <= 6):
                    return None, False
                scores.append(val)
            except (ValueError, TypeError):
                return None, False
        return sum(scores), True

    def generate_fix(
        self,
        *,
        referral_text: str,
        ambient_scribe_text: str,
        medication_list_json: str,
        gap_answers_json: str,
        current_oasis_gold_standard_json: str,
        audit_conflicts_text: str,
        model_id: str,
    ) -> dict:
        """Produce a revised oasis_gold_standard.json that resolves LLM audit conflicts.

        Uses OASIS_GOLD_STANDARD_FIX_PROMPT which shows all existing documents +
        the audit conflict summary and asks for a targeted JSON revision.
        """
        from app.config.prompts import OASIS_GOLD_STANDARD_FIX_PROMPT
        prompt = OASIS_GOLD_STANDARD_FIX_PROMPT.format(
            referral_text=referral_text[:6000],
            ambient_scribe_text=ambient_scribe_text[:4000],
            medication_list_json=medication_list_json[:2000],
            gap_answers_json=gap_answers_json[:3000],
            current_oasis_gold_standard_json=current_oasis_gold_standard_json[:6000],
            audit_conflicts_text=audit_conflicts_text,
        )
        response = self.bedrock.invoke_json(
            prompt=prompt,
            model_id=model_id,
            max_tokens=4096,
        )
        text = response["text"].strip()
        try:
            result = json.loads(extract_json_object(text))
        except Exception as exc:
            logger.error("generate_fix JSON parse failed (%s) — attempting repair", exc)
            result = repair_truncated_json(text)
        if not isinstance(result, dict):
            raise ValueError(f"generate_fix returned non-dict: {type(result)}")
        return result
