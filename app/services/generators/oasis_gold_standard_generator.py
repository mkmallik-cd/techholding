def _item_value(item_map: dict[str, dict], code: str) -> str | None:
    """Safely extract the string value of an item from an item_map."""
    item = item_map.get(code)
    if item is None:
        return None
    val = item.get("value")
    return str(val).strip() if val is not None else None


class OasisGoldStandardGenerator:
    """Step 6 generator: produces a full OASIS-E1 gold-standard assessment dict."""

    def __init__(self) -> None:
        # Initialise the shared Bedrock LLM client.
        self.bedrock = BedrockClient()
        # Load oasis_config.json for per-group coding guidance injection.
        self._oasis_config_groups: list[dict] = self._load_oasis_config()
        # Build field_code → group_guidance lookup (only real OASIS groups).
        self._field_guidance_map: dict[str, str] = {}
        self._field_group_name_map: dict[str, str] = {}
        for group in self._oasis_config_groups:
            guidance = group.get("group_guidance", "")
            group_name = group.get("group_name", "")
            if not guidance:
                continue
            for field in group.get("fields", []):
                self._field_guidance_map[field] = guidance
                self._field_group_name_map[field] = group_name

    @staticmethod
    def _load_oasis_config() -> list[dict]:
        """Load oasis_config.json from the app config directory.

        Returns empty list on failure so the generator degrades gracefully.
        """
        import os
        config_dir = os.path.join(os.path.dirname(__file__), "..", "..", "config")
        default_path = os.path.normpath(os.path.join(config_dir, "oasis_config.json"))
        path = os.environ.get("OASIS_CONFIG_PATH", default_path)
        try:
            with open(path, encoding="utf-8") as fh:
                data = json.load(fh)
            if isinstance(data, list):
                logger.info(
                    "OasisGoldStandardGenerator: loaded oasis_config with %d groups from %s",
                    len(data), path,
                )
                return data
        except FileNotFoundError:
            logger.warning(
                "OasisGoldStandardGenerator: oasis_config.json not found at %s — "
                "per-group coding guidance will not be injected",
                path,
            )
        except Exception as exc:
            logger.error("OasisGoldStandardGenerator: failed to load oasis_config: %s", exc)
        return []

    def _build_oasis_guidance_for_codes(self, codes: list[str]) -> str:
        """Return consolidated group_guidance text for all oasis_config groups
        whose fields overlap with *codes*.

        Groups are de-duplicated; only groups that have non-empty guidance and
        contain at least one real OASIS code (not non_oasis_*) are included.
        """
        seen_guidances: dict[str, str] = {}  # group_name → guidance
        for code in codes:
            group_name = self._field_group_name_map.get(code)
            guidance = self._field_guidance_map.get(code)
            if group_name and guidance and group_name not in seen_guidances:
                seen_guidances[group_name] = guidance
        if not seen_guidances:
            return ""
        parts = [
            f"### {name}\n{text}" for name, text in seen_guidances.items()
        ]
        return "\n\n".join(parts)

    def generate(
        self,
        *,
        referral_text: str,
        medication_list: dict,
        scribe_text: str | None,
        gap_answers: dict,
        metadata: dict,
        model_id: str,
        audit_context: str | None = None,
    ) -> dict:
        """Generate oasis_gold_standard.json content.

        Args:
            referral_text: Raw referral packet text from Step 2.
            medication_list: Medication list dict from Step 3.
            scribe_text: Ambient scribe text from Step 3b (or None).
            gap_answers: Gap answers dict from Step 4.
            metadata: Patient metadata dict from Step 1.
            model_id: AWS Bedrock model identifier string.
            audit_context: Optional conflict summary from a previous LLM audit run.

        Returns:
            Dict with keys: items, validation, source_documents, generated_at.
        """
        archetype = metadata.get("archetype", "chf_exacerbation")
        diagnosis_context = metadata.get("primary_diagnosis", archetype.replace("_", " ").title())

        # Step 1: Copy BIMS, PHQ, GG X1 admission codes, and M-ADL codes verbatim from
        #         gap_answers (never regenerated by LLM — PRD Step 4 is authoritative).
        bims_items = self._correct_bims_arithmetic(
            self._copy_from_gap_answers(gap_answers, BIMS_COPY_CODES)
        )
        phq_items = self._apply_phq2_gate(
            self._copy_from_gap_answers(gap_answers, PHQ_COPY_CODES)
        )
        gg_adl_items = self._expand_gg_adl_from_gap_answers(gap_answers)
        n0415_items = (
            self._derive_n0415_from_gap_sub_codes(gap_answers)
            or self._derive_n0415_from_medication_list(medication_list)
            or self._decompose_n0415_from_gap_answers(gap_answers)
        )
        logger.info(
            "Step 6: copied %d BIMS + %d PHQ + %d GG/ADL + %d N0415 items from gap_answers",
            len(bims_items),
            len(phq_items),
            len(gg_adl_items),
            len(n0415_items),
        )

        # Step 2: Run 5 LLM section batches for all remaining OASIS fields.
        scribe_section = (
            f"--- AMBIENT SCRIBE (Nurse Visit Note) ---\n{scribe_text}"
            if scribe_text
            else "(No ambient scribe available for this patient)"
        )
        medication_json = json.dumps(medication_list, indent=2) if medication_list else "(Medication list unavailable)"
        gap_context = self._build_gap_context(gap_answers)

        all_llm_items: list[dict] = []
        groups_completed = 0
        groups_failed = 0
        for section_name, codes in OASIS_SECTION_BATCHES:
            # PRD: Clinical archetype based filtering.
            # If patient is not wound-bearing, omit wound-specific codes from the batch request.
            filtered_codes = codes
            if archetype not in WOUND_ARCHETYPES:
                filtered_codes = [c for c in codes if c not in WOUND_CODES]

            if not filtered_codes:
                logger.info("Step 6: Skipping LLM batch %s (no relevant codes for archetype %s)", section_name, archetype)
                continue

            logger.info("Step 6: LLM batch %s (%d codes requested)", section_name, len(filtered_codes))
            batch_items = self._generate_and_fix_section_batch(
                codes=filtered_codes,
                section_name=section_name,
                archetype=archetype,
                diagnosis_context=diagnosis_context,
                referral_text=referral_text,
                scribe_section=scribe_section,
                medication_json=medication_json,
                gap_context=gap_context,
                has_scribe="Yes" if scribe_text else "No",
                model_id=model_id,
                audit_context=audit_context,
            )
            all_llm_items.extend(batch_items)
            groups_completed += 1
            logger.info("Step 6: batch %s produced %d items", section_name, len(batch_items))

        # Build item_map: LLM items first, then Gap-derived items overwrite.
        # Priority (highest wins): BIMS/PHQ > GG/ADL (gap_answers) > LLM
        item_map: dict[str, dict] = {}
        for item in all_llm_items:
            code = item.get("item_code", "")
            if code:
                item_map[code] = item
        for item in gg_adl_items + bims_items + phq_items + n0415_items:
            item_map[item["item_code"]] = item

        # Step 3: Apply 8 CMS deterministic skip-logic rules.
        item_map, rules_applied = self._apply_skip_logic(item_map)
        logger.info("Step 6: skip-logic applied %d rule(s): %s", len(rules_applied), rules_applied)

        # Convert to sorted list and build output.
        final_items = sorted(item_map.values(), key=lambda x: x.get("item_code", ""))

        # Step 4: Build validation block.
        validation = self._build_validation(final_items, rules_applied)
        logger.info(
            "Step 6: validation — bims_score=%s phq_score=%s hip_score=%s hip_valid=%s",
            validation["bims_score"],
            validation["phq_score"],
            validation["hip_score"],
            validation["hip_valid"],
        )

        # PRD Section 2.3 format: a flat key-value object mapping every OASIS item code to its correct value.
        # Format matches SYN_0024 reference: includes metadata fields + _items_detail rationale array.
        _METADATA_KEYS = {"patient_id", "generated_at", "use_rag", "groups_completed", "groups_failed", "total_fields"}
        output_dict: dict = {
            "_generated": True,
            "_note": "SYNTHETIC \u2014 NOT REAL PATIENT DATA",
            "patient_id": metadata.get("patient_id", "UNKNOWN"),
            "generated_at": datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%S.%f") + "Z",
            "use_rag": True,
            "groups_completed": groups_completed,
            "groups_failed": groups_failed,
        }

        # Non-OASIS narrative keys that should never appear in the gold standard output.
        # These are sometimes hallucinated by the LLM from EHR context.
        _NON_OASIS_BLOCKED: set[str] = {
            "PHQ_MOOD_INTERVIEW", "ALLERGIES", "VITAL_SIGNS", "DIABETIC_FOOT",
            "COMMUNITY_SCREENING", "CORRECTIVE_ACTION_PLAN", "CIRCULATORY_HISTORY",
            "MENTAL_STATUS", "SKIN", "ACTIVITIES_PERMITTED", "MEDICATIONS",
            "NUTRITIONAL_REQUIREMENTS", "SAFETY_MEASURES", "DISCHARGE_PLANNING",
        }
        
        # Parent codes that often have specific sub-codes and should be filtered out if null
        # to keep the JSON strictly terminal.
        # e.g. M1023 is a parent, M1023_1 is specific.
        parent_codes = {
            "K0520", "M1021", "M1023", "M1028", "M1030", "M1033", 
            "M1060", "M2102", "O0110", "D0150", "C0300", "C0400"
        }

        for item in final_items:
            code = item["item_code"]
            val = item.get("value")

            # Hard-block non-OASIS narrative codes
            if code in _NON_OASIS_BLOCKED:
                logger.warning("Step 6: dropping non-OASIS code %s from output", code)
                continue

            # Validate A1005 (Ethnicity) — must be "1" or "2", not a state code or other value
            if code == "A1005" and val is not None:
                val_str = str(val).strip()
                if val_str not in ("1", "2"):
                    logger.warning(
                        "Step 6: A1005 value %r invalid (expected 1 or 2 for ethnicity) — clearing",
                        val_str,
                    )
                    val = None

            # Validate A1010 (Race) — must be a 1-2 digit numeric code 1–6 or 99, not a date/ZIP/state
            if code == "A1010" and val is not None:
                val_str = str(val).strip()
                if "/" in val_str or len(val_str) > 3 or not val_str.isdigit():
                    logger.warning(
                        "Step 6: A1010 value %r looks invalid (expected race code 1–6 or 99) — clearing",
                        val_str,
                    )
                    val = None

            # Validate A1110 (Preferred language) — must be a 2-letter language code, not a date or name
            if code == "A1110" and val is not None:
                val_str = str(val).strip()
                import re as _re
                # Date pattern or purely numeric → wrong field
                if _re.match(r"\d{1,2}[/-]\d{1,2}[/-]\d{4}", val_str) or val_str.isdigit():
                    logger.warning(
                        "Step 6: A1110 value %r looks like a date/number, not a language code — clearing",
                        val_str,
                    )
                    val = None
                elif len(val_str) > 5:
                    # Language codes are 2-3 chars (EN, SP, OT); longer values are free-text
                    logger.warning(
                        "Step 6: A1110 value %r too long for a language code — clearing",
                        val_str,
                    )
                    val = None

            # Validate clinical date fields — must be a plausible care-episode date (year >= 2000).
            # LLMs sometimes place the patient DOB (e.g. "06/14/1946") into fields like M0104.
            _CLINICAL_DATE_CODES = {"M0080", "M0090", "M0100", "M0102", "M0104", "M0110", "M1005"}
            if code in _CLINICAL_DATE_CODES and val is not None:
                val_str = str(val).strip()
                import re as _re
                m_date = _re.match(r"(\d{1,2})[/-](\d{1,2})[/-](\d{4})$", val_str)
                if m_date:
                    year = int(m_date.group(3))
                    if year < 2000:
                        logger.warning(
                            "Step 6: %s value %r has year %d < 2000 — looks like a birthdate, clearing",
                            code, val_str, year,
                        )
                        val = None
            
            # Skip parent codes if they are null or empty (they have sub-codes like M1023_1)
            if code in parent_codes and (val is None or str(val).strip() == ""):
                continue

            output_dict[code] = val

        # Set total_fields = count of OASIS field codes only (excludes metadata and underscore keys).
        output_dict["total_fields"] = sum(
            1 for k in output_dict
            if not k.startswith("_") and k not in _METADATA_KEYS
        )

        # O0110 fields are binary 0/1 flags; normalise any "NA" the LLM wrote to "0".
        for key in list(output_dict.keys()):
            if key.startswith("O0110") and output_dict[key] == "NA":
                output_dict[key] = "0"

        # Append _items_detail: full rationale + confidence for every OASIS field.
        output_dict["_items_detail"] = [
            {
                "item_code": item["item_code"],
                "value": item.get("value"),
                "rationale": item.get("rationale", ""),
                "confidence": item.get("confidence", "high"),
                "status": "completed",
            }
            for item in final_items
            if item.get("item_code") and not item["item_code"].startswith("_")
        ]

        return output_dict

    # ── Private helpers ────────────────────────────────────────────────────────────────────

    @staticmethod
    def _apply_phq2_gate(phq_items: list[dict]) -> list[dict]:
        """Enforce the CMS PHQ-2 screening gate on a list of PHQ item dicts.

        Rule: If D0150A1 + D0150B1 < 3 the two-item screen is negative — items C–I
        (both symptom-presence X1 and frequency X2 codes) must be null/not-assessed,
        and D0160 is recalculated using only items A and B.

        Edits item dicts in-place and returns the same list.
        """
        by_code = {item["item_code"]: item for item in phq_items}

        a1_raw = by_code.get("D0150A1", {}).get("value")
        b1_raw = by_code.get("D0150B1", {}).get("value")
        if a1_raw is None or b1_raw is None:
            # Incomplete PHQ data — cannot evaluate gate
            return phq_items

        try:
            screen_score = int(str(a1_raw)) + int(str(b1_raw))
        except (ValueError, TypeError):
            return phq_items

        if screen_score >= 3:
            # Gate not triggered — full PHQ-9 was administered
            return phq_items

        # Gate fires: null out items C–I (symptoms not assessed)
        downstream_letters = ["C", "D", "E", "F", "G", "H", "I"]
        for letter in downstream_letters:
            for suffix in ("1", "2"):
                code = f"D0150{letter}{suffix}"
                item = by_code.get(code)
                if item is not None:
                    item["value"] = None
                    item["rationale"] = (
                        f"PHQ-2 screening score (D0150A1={a1_raw} + D0150B1={b1_raw} = "
                        f"{screen_score}) < 3 — PHQ-9 items C–I are not administered "
                        f"(CMS skip-logic gate). Original gap_answers value nulled by "
                        f"oasis_gold_standard_generator._apply_phq2_gate()."
                    )
                    item["confidence"] = "high"

        # Recalculate D0160: sum of frequency (X2) only for items present (X1 != "0") among A and B
        freq_sum = 0
        for letter in ("A", "B"):
            symptom = by_code.get(f"D0150{letter}1", {}).get("value")
            freq = by_code.get(f"D0150{letter}2", {}).get("value")
            if symptom is not None and str(symptom).strip() != "0" and freq is not None:
                try:
                    freq_sum += int(str(freq))
                except (ValueError, TypeError):
                    pass

        d0160 = by_code.get("D0160")
        if d0160 is not None:
            old_value = d0160.get("value")
            d0160["value"] = str(freq_sum)
            d0160["rationale"] = (
                f"PHQ-2 gate applied: screen score {screen_score} < 3. "
                f"D0160 recalculated as sum of frequency codes for present items A–B only "
                f"(was {old_value} in gap_answers; corrected to {freq_sum})."
            )
            d0160["confidence"] = "high"

        logger.info(
            "PHQ-2 gate applied (screen=%d): items C–I nulled, D0160 corrected to %d",
            screen_score,
            freq_sum,
        )
        return phq_items

    def _copy_from_gap_answers(self, gap_answers: dict, codes: list[str]) -> list[dict]:
        """Copy a list of codes verbatim from gap_answers.

        Args:
            gap_answers: Full gap answers dict from Step 4.
            codes: List of OASIS item codes to copy.

        Returns:
            List of item dicts with source="gap_answers".
        """
        items: list[dict] = []
        for code in codes:
            answer = _lookup_gap_answer(gap_answers, code)
            if answer is not None:
                # Resolve human-readable question text from field map or gap_answers
                meta = None
                try:
                    from app.config.oasis_field_map import OASIS_FIELD_MAP
                    meta = OASIS_FIELD_MAP.get(code)
                except Exception:
                    pass
                question_text = (meta["question"] if meta and meta.get("question") else code)
                items.append({
                    "item_code": code,
                    "value": str(answer) if answer is not None else None,
                    "rationale": (
                        f"Copied verbatim from tap_tap_gap_answers.json (Step 4 assessment). "
                        f"Question: {question_text}"
                    ),
                    "confidence": "high",
                    "source": "gap_answers",
                })
            else:
                logger.warning(
                    "Step 6: %s not found in gap_answers — skipping",
                    code,
                )
        return items

    @staticmethod
    def _correct_bims_arithmetic(bims_items: list[dict]) -> list[dict]:
        """Enforce two-level BIMS arithmetic consistency.

        Rules:
          C0300 = C0300A + C0300B + C0300C  (range 0–6)
          C0400 = C0400A + C0400B + C0400C  (range 0–6)
          C0500 = C0200  + C0300  + C0400   (range 0–15)

        Corrects C0300, C0400, and C0500 in-place if they don't match their sub-sums.
        Sub-code values are authoritative; grouping codes are derived.
        """
        by_code = {item["item_code"]: item for item in bims_items}

        def _int(code: str) -> int | None:
            item = by_code.get(code)
            if item is None:
                return None
            try:
                return int(str(item.get("value", "")).strip())
            except (ValueError, TypeError):
                return None

        def _fix(code: str, expected: int, sub_expr: str) -> None:
            item = by_code.get(code)
            if item is None:
                by_code[code] = {
                    "item_code": code,
                    "value": str(expected),
                    "rationale": f"BIMS arithmetic correction: {sub_expr} = {expected}",
                    "confidence": "high",
                    "source": "gap_answers",
                }
            else:
                current = item.get("value")
                if str(current) != str(expected):
                    logger.warning(
                        "Step 6: BIMS arithmetic mismatch — %s=%s but %s=%d; correcting",
                        code, current, sub_expr, expected,
                    )
                    item["value"] = str(expected)
                    item["rationale"] = (
                        f"BIMS arithmetic correction: {sub_expr} = {expected} "
                        f"(gap_answers had {current})."
                    )

        # Derive C0300 from sub-codes
        c0300a, c0300b, c0300c = _int("C0300A"), _int("C0300B"), _int("C0300C")
        if None not in (c0300a, c0300b, c0300c):
            _fix("C0300", c0300a + c0300b + c0300c, "C0300A+C0300B+C0300C")  # type: ignore[operator]

        # Derive C0400 from sub-codes
        c0400a, c0400b, c0400c = _int("C0400A"), _int("C0400B"), _int("C0400C")
        if None not in (c0400a, c0400b, c0400c):
            _fix("C0400", c0400a + c0400b + c0400c, "C0400A+C0400B+C0400C")  # type: ignore[operator]

        # Derive C0500 from C0200 + corrected C0300 + corrected C0400
        c0200 = _int("C0200")
        c0300 = _int("C0300")   # may have just been corrected
        c0400 = _int("C0400")   # may have just been corrected
        if None not in (c0200, c0300, c0400):
            _fix("C0500", c0200 + c0300 + c0400, "C0200+C0300+C0400")  # type: ignore[operator]

        # Return merged list (by_code may have new keys not in original list)
        return list(by_code.values())

    def _expand_gg_adl_from_gap_answers(self, gap_answers: dict) -> list[dict]:
        """Derive GG0130 X1 admission codes, GG0170 X1 admission codes, and M-ADL codes from
        Step 4 gap answers.

        PRD rule: GG admission performance values ARE validated in Step 4 and must
        propagate unchanged into the gold standard.  This method is authoritative —
        the LLM must not override these values.

        GG0130 is stored as a grouped dict {"Eating": "05", ...}; each label maps to
        a specific X1 code.  GG0170 is stored as {"A": "04", ...}; each key maps to
        the corresponding X1 code.  M-ADL codes are stored as individual entries.
        """
        items: list[dict] = []

        # ── GG0130 Self-Care X1 (admission performance) ───────────────────────────────────
        gg0130_letters = set()
        for letter_or_letters in GG0130_LABEL_TO_LETTER.values():
            if isinstance(letter_or_letters, list):
                gg0130_letters.update(letter_or_letters)
            else:
                gg0130_letters.add(letter_or_letters)

        gg0130_found = False
        for letter in sorted(gg0130_letters):
            code = f"GG0130{letter}1"
            val = _lookup_gap_answer(gap_answers, code)
            if val is not None:
                gg0130_found = True
                items.append({
                    "item_code": code,
                    "value": str(val),
                    "rationale": (
                        f"Copied verbatim from tap_tap_gap_answers.json (Step 4 assessment). "
                        f"PRD: GG admission performance is validated in Step 4."
                    ),
                    "confidence": "high",
                    "source": "gap_answers",
                })
        
        if not gg0130_found:
            # Fallback: old-format gap_answers store GG0130 with root field_code "GG0130"
            # but encode the sub-code letter in the title (e.g. "Self-Care: ... - GG0130A").
            seen_codes: set[str] = set()
            for section in gap_answers.get("sections", []):
                for q in section.get("questions", []):
                    if "GG0130" not in q.get("field_codes", []):
                        continue
                    answer = q.get("answer")
                    if answer is None:
                        continue
                    title = q.get("title", "")
                    m = re.search(r"\bGG0130([A-G])\b", title, re.IGNORECASE)
                    if not m:
                        continue
                    letter = m.group(1).upper()
