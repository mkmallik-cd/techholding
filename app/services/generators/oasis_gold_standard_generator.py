    def _derive_n0415_from_medication_list(self, medication_list: dict | None) -> list[dict] | None:
        """Derive N0415A–I flags deterministically from the medication_list dict.

        Returns a full 9-item list, or None if medication_list is empty/unavailable.
        """
        if not medication_list:
            return None
        try:
            layer1 = medication_list.get("layer1") or medication_list
            meds = layer1.get("medications") or []
            if not meds:
                return None
        except Exception:
            return None

        flags: dict[str, str] = {c: "0" for c in self._N0415_ALL_CODES}
        matched: list[str] = []
        try:
            for med in meds:
                name = (
                    med.get("name") or med.get("medication_name") or med.get("drug_name") or ""
                ).lower()
                dose_str = str(med.get("dose") or med.get("dosage") or "").lower()
                for keyword, code in self._N0415_MED_NAME_TO_CODE.items():
                    if keyword in name:
                        if flags[code] == "0":
                            matched.append(f"{name}→{code}")
                        flags[code] = "1"
                        break
                # Aspirin ≥325 mg → antiplatelet
                if "aspirin" in name:
                    try:
                        dose_val = float("".join(c for c in dose_str if c.isdigit() or c == "."))
                        if dose_val >= 325:
                            if flags["N0415D"] == "0":
                                matched.append(f"aspirin {dose_val}mg→N0415D")
                            flags["N0415D"] = "1"
                    except (ValueError, TypeError):
                        pass
        except Exception as exc:
            logger.warning("Step 6: N0415 medication scan error: %s", exc)

        # N0415I = "1" only when ALL A–H are "0"
        if all(flags[c] == "0" for c in self._N0415_ALL_CODES[:-1]):
            flags["N0415I"] = "1"
        logger.info(
            "Step 6: N0415 derived from medication_list — matched: %s, flags: %s",
            matched,
            {k: v for k, v in flags.items() if v == "1"},
        )
        items: list[dict] = []
        for code in self._N0415_ALL_CODES:
            items.append({
                "item_code": code,
                "value": flags[code],
                "rationale": (
                    f"N0415 flag deterministically derived from medication_list. "
                    f"{self._N0415_CODE_LABELS.get(code, code)}: "
                    f"{'present' if flags[code] == '1' else 'absent'}."
                ),
                "confidence": "high",
                "source": "medication_list",
            })
        return items

